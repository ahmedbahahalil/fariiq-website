// Get translation by key
function t(key) {
  const lang = getCurrentLanguage();
  return translations[lang][key] || translations['en'][key] || key;
}

// Get current language from localStorage or browser
function getCurrentLanguage() {
  const stored = localStorage.getItem('fariiq_lang');
  if (stored && translations[stored]) {
    return stored;
  }

  // Auto-detect from browser
  const browserLang = navigator.language || navigator.userLanguage;
  if (browserLang.startsWith('ar')) {
    return 'ar';
  }

  return 'en';
}

// Set language
function setLanguage(lang) {
  if (!translations[lang]) return;

  const previousLang = getCurrentLanguage();
  localStorage.setItem('fariiq_lang', lang);
  applyTranslations();

  if (previousLang !== lang && typeof gtag === 'function') {
    gtag('event', 'language_switch', {
      event_category: 'engagement',
      previous_language: previousLang,
      new_language: lang
    });
  }
}

// Toggle between languages
function toggleLanguage() {
  const current = getCurrentLanguage();
  const newLang = current === 'en' ? 'ar' : 'en';
  setLanguage(newLang);
}

// Apply all translations to the page
function applyTranslations() {
  const lang = getCurrentLanguage();
  const trans = translations[lang];

  // Update html attributes
  document.documentElement.lang = trans.lang;
  document.documentElement.dir = trans.dir;

  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (trans[key]) {
      el.textContent = trans[key];
    }
  });

  // Update all elements with data-i18n-placeholder attribute
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (trans[key]) {
      el.placeholder = trans[key];
    }
  });

  // Update all elements with data-i18n-html attribute (for mixed content)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const keys = el.getAttribute('data-i18n-html').split(',');
    let html = '';
    keys.forEach(key => {
      const trimmedKey = key.trim();
      if (trimmedKey === 'hero_title_highlight') {
        html += `<span>${trans[trimmedKey] || ''}</span>`;
      } else {
        html += trans[trimmedKey] || '';
      }
    });
    el.innerHTML = html;
  });

  // Update data-badge attribute for pricing cards
  document.querySelectorAll('[data-badge-key]').forEach(el => {
    const key = el.getAttribute('data-badge-key');
    if (trans[key]) {
      el.setAttribute('data-badge', trans[key]);
    }
  });

  // Update app links with language parameter
  document.querySelectorAll('a[href*="app.fariiq.com"]').forEach(link => {
    const base = link.href.split('?')[0];
    link.href = lang === 'en' ? base : base + '?lang=' + lang;
  });

  // Update font family for Arabic
  if (lang === 'ar') {
    document.body.style.fontFamily = "'Noto Sans Arabic', 'Noto Sans', -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, sans-serif";
  } else {
    document.body.style.fontFamily = "'Noto Sans', -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, sans-serif";
  }
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
  applyTranslations();
});